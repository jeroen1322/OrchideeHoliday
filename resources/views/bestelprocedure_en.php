<h1>ORDERINGPROCEDURE</h1>
<hr>

<h2>ORDERING WHEN LOGGED IN</h2>
<p>When you are logged in, you can place an order in the following way:
<ul class="bestelprocedure">
  <li>Add an item to your shoppingcart</li><br>
  <li>Click on the shoppingcart button. This will show you a overview of all the items, and the total cost of those items, that are in your shoppingcart.</li><br>
  <li>When you have all the articles you want placed in your shoppingcart, you need to press 'AFREKENEN'. This will bring you to a page where you can proceed with placing the order</li><br>
  <li>First you will need to choose the delivery method. This can be:
    <br> - Getting the order devibered to your house
    <br> of
    <br> - Picking it up youself
    <br> When you made a choise, you can click 'VOLGENDE'
  </li><br>
  <li>You will now see a input field where you can place a comment regarding the order. This comment can be a resired delivery time. This field is optional.<br>
      You can (after typing the comment) press on 'VOLGENDE' to go to the order overview.
  </li><br>
  <li>You are now on the overview page. On this page you can see all the information regarding the order, including all the items in the shopping cart and their (total) price. You can also see the deliverymethod that you chose.
    <br>You can print this overview out by clickin on the print button.
    <br>From this page you can choose to either cancel the order, or complete the order and make the payment complete. If you choose to delete the order, that will mean that you shoppingcart will be cleared.
  </li>
</ul>
</p>

