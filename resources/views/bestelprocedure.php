<h1>BESTELPROCEDURE</h1>
<hr>

<h2>INGELOGD BESTELLEN</h2>
<p>Wanneer u ingelogd bent, kunt u op de volgende manier een bestelling plaatsen:
<ul class="bestelprocedure">
  <li>U plaats een artikel in uw winkelmand</li><br>
  <li>U klikt op de Winkelmand knop. Dit brengt u naar een overzicht van de artikelen die u in uw 'Winkelmand' heeft staan.</li><br>
  <li>Wanneer u alle arikelen die u wilt bestellen in uw winkelmand heeft, klikt u op 'AFREKENEN'. Dit brengt u naar het afreken scherm</li><br>
  <li>Eerst kiest u op het afreken-scherm de bezorgwijze die u wilt gebruiken. Dit kan zijn:
    <br> -Laten bezorgen
    <br> of
    <br> - Zelf ophalen
    <br> Nadat u uw keuze hebt gemaakt, klikt u op 'VOLGENDE'
  </li><br>
  <li>U krijgt nu een opmerking invoerveld te zien. In dit veld kunt u eventuele opmerking invoeren, die wij bij uw bestelling zullen ontvangen. Dit kan bijvoorbeeld een gewenste afleverdatum zijn. De opmerking is optioneel.<br>
     U kunt op 'VERDER' klikken om naar de volgende pagina te gaan.
  </li><br>
  <li>U bevind zich nu op de overzicht pagina. Hier ziet u alle (informatie over de) artikelen in uw bestelling, en de totaalprijs. Ook ziet u de door u geselecteerde bezorgwijze.
    <br> U kunt dit overzicht uitprinten door middel van op de print-knop te klikken.
    <br>Vanaf deze pagina kunt u definitief kiezen of u de bestelling wilt afronden en wilt betalen, of dat u de bestelling wilt annuleren. Als u de bestelling annuleerd zal uw winkelmand ook worden geleegd.
  </li>
</ul>
</p>

<h2>BESTELLEN WANNEER U NIET BENT INGELOGD</h2>
<p>Wanneer u nog niet bent ingelogd, kunt u op de volgende manier een bestelling plaatsen:
  <ul class="bestelprocedure">
    <li>U plaats een artikel in uw winkelmand</li><br>
    <li>U klikt op de Winkelmand knop. Dit brengt u naar een overzicht van de artikelen die u in uw 'Winkelmand' heeft staan.</li><br>
    <li>Wanneer u alle arikelen die u wilt bestellen in uw winkelmand heeft, klikt u op 'AFREKENEN'. Dit brengt u naar het afreken scherm</li><br>
    <li>Op dit scherm kunt u kiezen om in te loggen, als u al een account hebt. Ook kunt u kiezen om een nieuw account te registreren.<br>
      Wanneer u inlogt of een nieuw account registreert, zullen uw artikelen die u in uw winkelmand had staan automatisch worden overgezet naar de winkelmand van uw (nieuwe) account.<br>
      U kunt er eventueel voor kiezen om 'anoniem' te bestellen. Dit betekend dat u geen account hoeft te hebben. Wanneer u anoniem besteld zal u op de overzicht pagina uw e-mail adres en uw adres gegevens moeten invoeren. U ontvangt dan via de mail een factuur waar eerst aan moet worden voldaan voordat de bestelling als afgerond word gezien.
    </li><br>
    <li>Wanneer u uw keuze hebt gemaakt en door gaat met bestellen, krijgt u de keuze om de gewenste bezorgwijze te selecteren:
      <br> -Laten bezorgen
      <br> of
      <br> - Zelf ophalen
      <br> Nadat u uw keuze hebt gemaakt, klikt u op 'VOLGENDE'
    </li><br>
    <li>U krijgt nu een opmerking invoerveld te zien. In dit veld kunt u eventuele opmerking invoeren, die wij bij uw bestelling zullen ontvangen. Dit kan bijvoorbeeld een gewenste afleverdatum zijn. De opmerking is optioneel.<br>
       U kunt op 'VERDER' klikken om naar de volgende pagina te gaan.
    </li><br>
    <li>U bevind zich nu op de overzicht pagina. Hier ziet u alle (informatie over de) artikelen in uw bestelling, en de totaalprijs. Ook ziet u de door u geselecteerde bezorgwijze.
      <br> U kunt dit overzicht uitprinten door middel van op de print-knop te klikken.
      <br>Vanaf deze pagina kunt u definitief kiezen of u de bestelling wilt afronden en wilt betalen, of dat u de bestelling wilt annuleren. Als u de bestelling annuleerd zal uw winkelmand ook worden geleegd.
    </li>
  </ul>
</p>
